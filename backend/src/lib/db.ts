export async function getBurnHistory(
  tokenId: string,
  options: { skip?: number; take?: number } = {}
) {
  return prisma.burnRecord.findMany({
    where: { tokenId },
    orderBy: { timestamp: "desc" },
    skip: options.skip ?? 0,
    take: options.take ?? 20,
  });
}

